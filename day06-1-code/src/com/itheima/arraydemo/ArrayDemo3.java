package com.itheima.arraydemo;

public class ArrayDemo3 {
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 4, 5};

        for (int i = 0; i <= 4; i++) {
            System.out.println(array[i]);
        }

        //求数组长度：数组名.length
        System.out.println(array.length);

        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }

        //扩展：idea提供的快速生成数组遍历方式
        //数组名.fori
        //快速循环：终止条件.fori




    }
}
